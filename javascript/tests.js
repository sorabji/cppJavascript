
function multiplyTable(){
    document.write("<table border=\"1\">");
    var i=1;
    var j=1;
    for (i;i<=10;i++){
	document.write("<tr>");
	for (j;j<=10;j++){
	    document.write("<td>");
	    document.write(j*i);
	    document.write("</td>");
	}
    j=1;
    }
    return 0;
}


